def ocean(wave,frequency,size): #creates blue squiggly lines that are layered - Ivan
    IVT.pensize(size)
    IVT.penup()
    IVT.goto(0,0)
    IVT.pendown()
    IVT.right(90)
    for i in range(45):
        randblu=random.randint(75,255)
        randgrn=random.randint(0,50)
        IVT.pencolor((0,randgrn,randblu))
        for i in range(13):
            IVT.circle(wave,180)
            IVT.forward(frequency)
            IVT.right(180)
            IVT.circle(wave,-180)
            IVT.right(180)
            IVT.forward(frequency)
        IVT.penup() #shifts waves down one row
        IVT.right(90)
        IVT.forward(520)
        IVT.left(90)
        IVT.pendown()
        IVT.forward(size)
