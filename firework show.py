def backround(): #creates a night sky with randomized stars - Renzo
    Renzo.backward(300)
    Renzo.dot(1000000, "darkblue")
    Renzo.dot(1000, "blue")
    Renzo.dot(700, "#0487DE")
    for i in range(100):
        sizerand=random.randint(5,10)
        locorand1=random.randint(-500,500)
        locorand2=random.randint(150,400)
        Renzo.penup()
        Renzo.goto(locorand1, locorand2)
        Renzo.pendown()
        color=random.randint(1,2)
        if color == 1:
            Renzo.dot(sizerand, 'white')
        if color==2:
            Renzo.dot(sizerand,'yellow')
