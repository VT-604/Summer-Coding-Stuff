#Initialization
import turtle
IVT = turtle.Turtle()
nora = turtle.Turtle()
IVT.speed(10)
nora.speed(10)
#Functions
def catHead():
    IVT.goto(0,0)
    IVT.circle(125,360)
def catBody():
    IVT.penup()
    IVT.goto(35,50)
    IVT.right(30)
    IVT.pendown()
    IVT.left(225)
    IVT.circle(175,90)
    IVT.circle(100,90)
    IVT.circle(175,90)
    IVT.circle(100,90)
def catLeg():
    IVT.right(25)
    IVT.circle(100,70)
    IVT.circle(20,110)
    IVT.circle(100,70)
    IVT.circle(19,110)
def catFoot():
    IVT.circle(15,110)
    IVT.circle(75,70)
    IVT.circle(14,110)
    IVT.circle(75,70)

