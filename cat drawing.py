def drawCat():
    IVT.speed(10)
    IVT.fillcolor("black")
    IVT.begin_fill()
    catHead()
    IVT.end_fill()
    
    IVT.penup() #back left paw
    IVT.goto(50,-230)
    IVT.pendown()
    IVT.left(30)
    catFoot()
    
    IVT.begin_fill() #torso
    catBody()
    IVT.end_fill()

    IVT.fillcolor("white")
    IVT.begin_fill()
    IVT.penup()
    IVT.goto(150,-220)
    IVT.pendown() #back right paw
    catFoot()
    IVT.end_fill()
    
    IVT.penup() #back right leg
    IVT.goto(175,-100)
    IVT.pendown()
    IVT.left(35)
    IVT.fillcolor("black")
    IVT.begin_fill()
    catLeg()
    IVT.end_fill()
    
    IVT.penup() #front left leg
    IVT.goto(10,-120)
    IVT.pendown()
    IVT.begin_fill()
    catLeg()
    IVT.end_fill()
    
    IVT.penup() # front right leg
    IVT.goto(120,-130)
    IVT.pendown()
    IVT.left(60)
    IVT.begin_fill()
    catLeg()
    IVT.end_fill()

    IVT.fillcolor("white")
    IVT.begin_fill()
    IVT.penup()#front right paw
    IVT.goto(115,-230)
    IVT.pendown()
    IVT.right(30)
    catFoot()
    IVT.end_fill()

    IVT.begin_fill()
    IVT.penup()#front left paw
    IVT.goto(-55,-230)
    IVT.pendown()
    IVT.right(0)
    catFoot()
    IVT.end_fill()

    IVT.fillcolor("black")
    IVT.begin_fill() #torso again
    IVT.right(180)
    catBody()
    IVT.end_fill()

    EarsandTail()
    drawEyes()
    drawface()
    
#Main
drawCat()
